from math import *

print(-2)
print(2)
print(2.097)

my_num = 5
print(my_num)

my_new_num = -5

# convert a number into a string, need it to print a number next to a string
print(str(my_num) + ' my favourite number')

# get the absolute value of a number
print(abs(my_new_num))

# get the power of a number, takes in 2 arguments
print(pow(my_num, 2))

# return the larger number back to us
print(max(1, 5, 10))

# return the smallest number back to us
print(min(1, 5, 10))

# round a number up or down depending on the number
print(round(3.2))
print(round(3.7))

# floor a number
print(floor(3.7))

# ceil a number
print(ceil(3.7))

# sqrt a number
print(sqrt(36))